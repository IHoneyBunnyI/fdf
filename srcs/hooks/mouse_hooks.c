#include "fdf.h"

int	mouse_down_hook(int button, int x, int y, void *keys_old)
{
	t_keys	*keys;

	keys = keys_old;
	(void)x;
	(void)y;
	(void)keys;
	if (button == 1)
		keys->left_mouse = 1;
	if (button == 2)
		keys->right_mouse = 1;
	if (button == 3)
		keys->wheel = 1;
	if (button == 5)
		keys->mouse_zoom = 1;
	if (button == 4)
		keys->mouse_zoom = -1;
	return (1);
}

int	mouse_up_hook(int button, int x, int y, void *keys_old)
{
	t_keys	*keys;

	(void)x;
	(void)y;
	keys = keys_old;
	if (button == 1)
		keys->left_mouse = 0;
	if (button == 2)
		keys->right_mouse = 0;
	if (button == 3)
		keys->wheel = 0;
	return (1);
}

int	mouse_move(int x, int y, void *fdf_old)
{
	t_fdf	*fdf;
	int		prev_x;
	int		prev_y;

	fdf = fdf_old;
	prev_x = fdf->mouse_x;
	prev_y = fdf->mouse_y;
	fdf->mouse_x = x;
	fdf->mouse_y = y;
	if (fdf->keys->left_mouse)
	{
		fdf->camera->beta += (x - prev_x) * 0.004;
		fdf->camera->alpha += (y - prev_y) * 0.004;
	}
	if (fdf->keys->right_mouse)
	{
		fdf->camera->offset_x += (x - prev_x) / 2;
		fdf->camera->offset_y += (y - prev_y) / 2;
	}
	if (fdf->keys->wheel)
		fdf->camera->gamma += (x - prev_x) * 0.004;
	return (0);
}
