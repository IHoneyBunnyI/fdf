#include "fdf.h"
#include <math.h>

static void	set_isometric(t_fdf *fdf)
{
		fdf->camera->offset_x = 0;
		fdf->camera->offset_y = 0;
		fdf->map->square_size = \
	ft_min(WIDTH / fdf->map->width_map / 2, HEIGHT / fdf->map->height_map / 2);
		fdf->camera->alpha = 0.;
		fdf->camera->beta = 0.;
		fdf->camera->gamma = 0;
		fdf->camera->z_coefficient = 1;
}

static void	calculate_point(t_point *p, t_fdf *fdf)
{
	p->x *= fdf->map->square_size;
	p->y *= fdf->map->square_size;
	p->z *= fdf->map->square_size * fdf->camera->z_coefficient;
	p->x -= (fdf->map->width_map * fdf->map->square_size) / 2;
	p->y -= (fdf->map->height_map * fdf->map->square_size) / 2;
}

static void	set_parralel(t_point *p)
{
	int		prev_1_x;
	int		prev_1_y;

	prev_1_x = p->x;
	prev_1_y = p->y;
	p->x = (prev_1_x - prev_1_y) * cos(0.523599);
	p->y = (prev_1_x + prev_1_y - p->z) * sin(0.523599);
}

static void	set_parralel2(t_fdf *fdf)
{
	fdf->camera->alpha = 0;
	fdf->camera->beta = 0;
	fdf->camera->gamma = 0;
}

t_point	set_point(t_fdf *fdf, t_point p)
{
	float	prev_a;
	float	prev_b;
	float	prev_g;

	if (fdf->keys->is_isometric)
		set_isometric(fdf);
	prev_a = fdf->camera->alpha;
	prev_b = fdf->camera->beta;
	prev_g = fdf->camera->gamma;
	if (fdf->keys->is_parralel)
		set_parralel2(fdf);
	calculate_point(&p, fdf);
	rotate_x(&p, fdf->camera->alpha);
	rotate_y(&p, fdf->camera->beta);
	rotate_z(&p, fdf->camera->gamma);
	if (!fdf->keys->is_parralel)
		set_parralel(&p);
	fdf->camera->alpha = prev_a;
	fdf->camera->beta = prev_b;
	fdf->camera->gamma = prev_g;
	p.x += (WIDTH / 2) + fdf->camera->offset_x;
	p.y += ((HEIGHT + fdf->map->height_map * fdf->map->square_size / 2) / 2) \
		+ fdf->camera->offset_y;
	return (p);
}
